int main(){
int m[8]={2,546,75,12,1000,9999,51,807};
int luck = 0;	//* кількість "щасливих" квитків
int non_luck = 0;	//* кількість звичайних квитків
for( int i = 0; i <= 7; i++){
	int n = 0;
	n = m[i] / 1000;	//* перевірка квитків на "щасливі" та звичайні
	if ( n != 0){
	luck = luck + 1;
	}else{
	non_luck = non_luck + 1;
	}
}
return 0;
}

